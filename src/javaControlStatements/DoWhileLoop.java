package javaControlStatements;

public class DoWhileLoop {

	public static void main(String[] args) {
		// ex-1
		//1...10 numbers
		int i=1;
		do
		{
			System.out.println(i);  
			i++;
			
		}while(i<=10);
//ex-2
		//print 10 - 1
		int j=10;
		do {
			System.out.println(j);  
			j--;
		}while(j>=1);
	}

}
